    mask[:, :5] = 0.0      # Mask prompt
    mask[:, 10:15] = 0.0   # Mask <information> snippet
    
    print(f"Masking Indicator (Trajectory 0): {mask[0].tolist()}")
    
    # 5. Initialize and Compute GRPO Loss
    print("\nComputing GRPO Objective Function...")
    grpo_criterion = GRPOLoss(clip_epsilon=0.2, beta=0.01, alpha=0.05)
    
    loss, metrics = grpo_criterion(
        logits_theta=logits_theta,
        logits_old=logits_old,
        logits_ref=logits_ref,
        actions=actions,
        advantages=advantages,
        mask=mask
    )
    
    print("\nCalculated Loss Components:")
    for key, val in metrics.items():
        print(f"  - {key}: {val:.4f}")
        
    # 6. Verify Backpropagation works flawlessly
    print("\nExecuting backward pass (Policy Optimization)...")
    loss.backward()
    
    if logits_theta.grad is not None:
        print("Success! Gradients computed for Policy LLM (\pi_\theta).")
        print(f"Gradient norm: {logits_theta.grad.norm().item():.4f}")
