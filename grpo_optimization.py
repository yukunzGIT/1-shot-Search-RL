    print(f"Group Rewards:    {mock_rewards.tolist()}")
    print(f"Group Advantages: {advantages.tolist()}")
    
    # 3. Simulate Model Logits and Actions
    # Requires_grad=True simulates the Policy LLM we are optimizing
    logits_theta = torch.randn(G, T, V, requires_grad=True) 
    # Old policy and Reference policy are frozen (no grad)
    logits_old = logits_theta.detach().clone() + 0.05 * torch.randn(G, T, V) 
    logits_ref = logits_theta.detach().clone() + 0.1 * torch.randn(G, T, V)
    
    # Simulate generated token actions
    actions = torch.randint(0, V, (G, T))
    
    # 4. Simulate the Masking Indicator (M_{i,t})
